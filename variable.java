public class Main
{
       public static void main (String[] args)
       {
              boolean a = false;      //8 bit 	true/false
              byte b = 2;             //8 bit 	-27 bis 27-1
              short c = 34;           //16 bit 	-215 bis 215-1
              char d = 'A';          //16 bit 	0 bis 65535
              int e = 3;              //32 bit 	-231 bis 231-1
              float f = 1.00f;        //32 bit 	+/-1,4E-45 bis +/-3,4E+38
              long g = 3123;          //64 bit 	-263 bis 263-1
              double h = 34.42;       //64 bit 	+/-4,9E-324 bis +/-1,7E+308
              System.out.println(a);
       }
}
